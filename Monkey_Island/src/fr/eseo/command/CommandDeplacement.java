package fr.eseo.command;

/**
 * The class CommandDeplacement of the design pattern Command
 *
 */
public class CommandDeplacement implements Command {
	
	private Manager action;
	
	/**
	 * Instantiates a new command deplacement
	 * @param action the action
	 */
	public CommandDeplacement(Manager action){
	  this.action = action;
	}
	
	/**
	 * Execute the action
	 */
	public void execute(){
	  action.movePirate();
	}
}
